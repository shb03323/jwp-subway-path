package subway.presentation;

import com.fasterxml.jackson.databind.ObjectMapper;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Nested;
import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.autoconfigure.web.servlet.WebMvcTest;
import org.springframework.boot.test.mock.mockito.MockBean;
import org.springframework.dao.EmptyResultDataAccessException;
import org.springframework.http.MediaType;
import org.springframework.test.web.servlet.MockMvc;
import subway.business.StationService;
import subway.exception.StationNotFoundException;
import subway.presentation.dto.request.StationRequest;
import subway.presentation.dto.response.StationResponse;

import java.util.List;

import static org.mockito.ArgumentMatchers.*;
import static org.mockito.BDDMockito.given;
import static org.mockito.Mockito.doThrow;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.*;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.*;

@WebMvcTest(StationController.class)
public class StationControllerTest {

    @Autowired
    private MockMvc mockMvc;

    @Autowired
    private ObjectMapper objectMapper;

    @MockBean
    private StationService stationService;

    @Nested
    @DisplayName("역 추가 - POST /stations")
    class Create {

        @Test
        @DisplayName("성공")
        void success() throws Exception {
            // given
            final long id = 1L;
            final String name = "혜화";
            final StationRequest request = new StationRequest(name);
            final String requestBody = objectMapper.writeValueAsString(request);
            final StationResponse response = new StationResponse(id, name);
            given(stationService.save(any())).willReturn(response);

            // when, then
            mockMvc.perform(post("/stations")
                            .content(requestBody)
                            .contentType(MediaType.APPLICATION_JSON_VALUE))
                    .andExpect(header().string("Location", "/stations/1"))
                    .andExpect(status().isCreated());
        }

        @Test
        @DisplayName("실패 - 존재하지 않는 id")
        void fail_id_not_found() throws Exception {
            // given
            final long id = 1L;
            final String name = "혜화";
            final StationRequest request = new StationRequest(name);
            final String requestBody = objectMapper.writeValueAsString(request);
            given(stationService.save(any())).willThrow(EmptyResultDataAccessException.class);

            // when, then
            mockMvc.perform(post("/stations")
                            .content(requestBody)
                            .contentType(MediaType.APPLICATION_JSON_VALUE))
                    .andExpect(status().isBadRequest());
        }

    }

    @Nested
    @DisplayName("역 전체 조회 - GET /stations")
    class ReadAll {

        @Test
        @DisplayName("성공")
        void success() throws Exception {
            // given
            final List<StationResponse> responses = List.of(
                    new StationResponse(1L, "잠실"),
                    new StationResponse(2L, "잠실새내"),
                    new StationResponse(3L, "종합운동장"));
            given(stationService.findAll()).willReturn(responses);

            // when, then
            final String responseBody =
                    "[" +
                            "{\"id\":1,\"name\":\"잠실\"}," +
                            "{\"id\":2,\"name\":\"잠실새내\"}," +
                            "{\"id\":3,\"name\":\"종합운동장\"}" +
                            "]";
            mockMvc.perform(get("/stations"))
                    .andExpect(status().isOk())
                    .andExpect(content().json(responseBody));
        }

    }

    @Nested
    @DisplayName("역 조회 - GET /stations/{id}")
    class Read {

        @Test
        @DisplayName("성공")
        void success() throws Exception {
            // given
            final long id = 1L;
            final StationResponse response = new StationResponse(1L, "잠실");
            given(stationService.findById(id)).willReturn(response);

            // when, then
            final String responseBody = "{\"id\":1,\"name\":\"잠실\"}";
            mockMvc.perform(get("/stations/1"))
                    .andExpect(status().isOk())
                    .andExpect(content().json(responseBody));
        }

        @Test
        @DisplayName("실패 - 존재하지 않는 id")
        void fail_id_not_found() throws Exception {
            // given
            final long id = 10L;
            given(stationService.findById(id)).willThrow(EmptyResultDataAccessException.class);

            // when, then
            mockMvc.perform(get("/stations/10"))
                    .andExpect(status().isBadRequest());
        }

    }

    @Nested
    @DisplayName("역 수정 - PUT /stations/{id}")
    class Update {

        @Test
        @DisplayName("성공")
        void success() throws Exception {
            // given
            final StationRequest request = new StationRequest("잠실");
            final String requestBody = objectMapper.writeValueAsString(request);

            // when, then
            mockMvc.perform(put("/stations/1")
                            .content(requestBody)
                            .contentType(MediaType.APPLICATION_JSON_VALUE))
                    .andExpect(status().isOk());
        }

        @Test
        @DisplayName("실패 - 존재하지 않는 id")
        void fail_id_not_found() throws Exception {
            // given
            final long id = 10L;
            final StationRequest request = new StationRequest("잠실");
            final String requestBody = objectMapper.writeValueAsString(request);
            doThrow(StationNotFoundException.class)
                    .when(stationService)
                    .update(eq(id), isA(StationRequest.class));

            // when, then
            mockMvc.perform(put("/stations/10")
                            .content(requestBody)
                            .contentType(MediaType.APPLICATION_JSON_VALUE))
                    .andExpect(status().isBadRequest());
        }

    }

    @Nested
    @DisplayName("역 삭제 - DELETE /stations/{id}")
    class Delete {

        @Test
        @DisplayName("성공")
        void success() throws Exception {
            mockMvc.perform(delete("/stations/1"))
                    .andExpect(status().isNoContent());
        }

        @Test
        @DisplayName("실패 - 존재하지 않는 id")
        void fail_id_not_found() throws Exception {
            // given
            final long id = 10L;
            doThrow(StationNotFoundException.class)
                    .when(stationService)
                    .delete(eq(id));

            // when, then
            mockMvc.perform(delete("/stations/10"))
                    .andExpect(status().isBadRequest());
        }

    }

}
